import sys

def binary_search(a, x):
    lo = 0
    hi = len(a)
    while lo < hi:
        mid = (lo+hi)//2
        midval = a[mid]
        if midval < x:
            lo = mid+1
        elif midval > x: 
            hi = mid
        else:
            return mid
    return -1
def linear_search(a, x):
    for i in range(len(a)):
        if a[i] == x:
            return i
    return -1

